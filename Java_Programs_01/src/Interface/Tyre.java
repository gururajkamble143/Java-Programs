package Interface;

interface Movable1
{
	boolean ismovable();
}
interface rollable
{
	boolean isrollable();
}

public class Tyre implements Movable1, rollable 
{
	public boolean ismovable()
	{
		return true;
	}
	public boolean isrollable()
	{
		return true;
	}
	public static void main(String[] args)
	{
		Tyre t=new Tyre();
		System.out.println(t.ismovable());
		System.out.println(t.isrollable());
	}
}
